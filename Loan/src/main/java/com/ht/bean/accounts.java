import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.experimental.Accessors;

/**
 * 账户表（accounts）
 * 	aid（账户id）
 * 	uid（用户id）
 * 	countmoney（总资产）
 * 	countbj（代收本金（总））
 * 	countlx（代收利息）
 * 	djmoney（冻结资金）
 * 	countsy（累计收益）
 * 	Invitedsy（好友邀请收益）
 * 	qtsy（其他收益）
 * 	kyye（可用余额）
 */

    @Data
    @NoArgsConstructor
    @Accessors(chain = true
    )
public class accounts {
    private int aid;
    private int uid;
    private float countmoney;
    private float countbj;
    private float countlx;
    private float djmoney;
    private float countsy;
    private float invitedsy;
    private float qtsy;
    private float kyye;
}
