package com.ht.bean;

public class Law {

    /*create table lawlist(
lid int primary key,
uid int null,
title varchar(100) null,
context mediumtext null,
cdate varchar(100) null,
state int null
)*/
    private Integer lid;
    private Integer empid;
    private String title;
    private String context;
    private String cdate;
    private Integer state;

    public Law() {
    }


    @Override
    public String toString() {
        return "Law{" +
                "lid=" + lid +
                ", empid=" + empid +
                ", title='" + title + '\'' +
                ", context='" + context + '\'' +
                ", cdate='" + cdate + '\'' +
                ", state=" + state +
                '}';
    }

    public Law(Integer lid, Integer empid, String title, String context, String cdate, Integer state) {
        this.lid = lid;
        this.empid = empid;
        this.title = title;
        this.context = context;
        this.cdate = cdate;
        this.state = state;
    }

    public Integer getLid() {
        return lid;
    }

    public void setLid(Integer lid) {
        this.lid = lid;
    }

    public Integer getEmpid() {
        return empid;
    }

    public void setEmpid(Integer empid) {
        this.empid = empid;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContext() {
        return context;
    }

    public void setContext(String context) {
        this.context = context;
    }

    public String getCdate() {
        return cdate;
    }

    public void setCdate(String cdate) {
        this.cdate = cdate;
    }

    public Integer getState() {
        return state;
    }

    public void setState(Integer state) {
        this.state = state;
    }
}
