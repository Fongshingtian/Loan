import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.experimental.Accessors;

/**
 * 资金流动记录表（record）
 * 	rid（id）
 *  uid（用户id）
 * 	rtime（操作时间）
 * 	rtype（操作类型）（1：投资，2：奖励，3：充值，4；体现，5：申请体现）
 * 	rmoney（操作金额）
 * 	kyye（可用余额）
 * 	bzxx(备注信息)
 */
@Data
@NoArgsConstructor
@Accessors(chain = true
)
public class record {
    private Integer rid;
    private Integer uid;
    private String rtime;
    private String rtype;
    private float rmoney;
    private float kyye;
    private String bzxx;
}
