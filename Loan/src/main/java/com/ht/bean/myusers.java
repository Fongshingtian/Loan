import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.experimental.Accessors;

/**
 * 用户表（myusers）（查 删）
 * 	uid （用户id）
 * 	uname（姓名）
 * 	phone（手机号）
 * 	loginpwd （登入密码）
 * 	zcdata（注册时间）
 * 	realname 实名认证（true false 两个状态）
 * 	email （邮箱认证）
 * 	urgentstate（紧急联系人状态 1：信息完善，2信息不完善）
 *  mycode 	我的邀请码
 *  tjrcode 推荐人邀请码
 *  //方仕顺
 *  point 积分
 * growthValue 成长值
 */
@Data
@NoArgsConstructor
@Accessors(chain = true
)
public class myusers {
    private  int uid;
    private String uname;
    private String phone;
    private String loginpwd;
    private String zcdata;
    private String realname;
    private String email;
    private String urgentstate;
    private String mycode;
    private String tjrcode;
    private int point;
    private int growthValue;

}
